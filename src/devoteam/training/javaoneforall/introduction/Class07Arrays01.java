package devoteam.training.javaoneforall.introduction;

public class Class07Arrays01 {
    public static void main(String[] args) {
        int[] ages = new int[3];
        ages[0] = 25;
        ages[1] = 35;
        ages[2] = 45;
        System.out.println("Age 1 is " + ages[0]);
        System.out.println("Age 2 is " + ages[1]);
        System.out.println("Age 3 is " + ages[2]);
    }
}
